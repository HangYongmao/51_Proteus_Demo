#include "includes.h"

#define SL P3_7					//串口接收启动端口：0->1启动。
#define LEDPORT P2

void main()
{
	unsigned char ucCount;

	SCON=0x10;				   //设置串口模式：模式0，允许接收。

	while(1)
		{
		SL=0;
		SL=1;				  //启动接收。

		while(RI==0);
		LEDPORT=SBUF;			  //转存数据。
		RI=0;

		for(ucCount=0;ucCount<250;ucCount++);
		}
} 