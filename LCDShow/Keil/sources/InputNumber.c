unsigned char ucGetNum(unsigned char ucKeyCode)
{
		switch(ucKeyCode)
			{
			case 11:return '7';break;
			case 12:return '8';break;
			case 13:return '9';break;
			case 21:return '4';break;
			case 22:return '5';break;
			case 23:return '6';break;
			case 31:return '1';break;
			case 32:return '2';break;
			case 33:return '3';break;
			case 42:return '0';break;

			default:break;
			}
		return 0xFF;
}